            <p>La cité des sables est un événement orienté PVP créé par le youtubeur Zelvac. Le but du jeu est de récupérer le maximum d’émeraudes afin de devenir le roi ou la reine de ladite cité. Pour ce faire, plusieurs équipes doivent récolter des ressources et les échanger avec les marchands de la ville contre des émeraudes, le tout sans que leur base ne se fasse piller ou sans qu’ils ne se fassent attaquer en chemin par les autres équipes !</p>
            <h1 class="titleActivities"> Pourquoi « RPG » ?</h1>
            <p> Grands amateurs de jeux type RPG, nous avons décidé de centrer la Cité des Elfes sur ce thème. Ainsi, le principe de l’événement a été entièrement revisité et remis au goût du jour en y incorporant une myriade d’éléments transformant le classique de Zelvac en un jeu totalement différent. Donjons, Quêtes, Succès, Succès cachés, Lore, Boss et bien plus ont été savamment tissés pour former la toile d’un divertissement nouveau et exclusif ! Nous espérons que celle-ci vous plaira !
