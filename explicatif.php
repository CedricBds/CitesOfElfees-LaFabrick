            <h1 class="titleActivities">Les coffres à émeraudes
            </h1>
            <p>C’est un grand classique des jeux RPG et nous n’allions pas passer à côté : les coffres ! Vous retrouverez des coffres dans toutes les zones de la cité, ainsi que dans tous les donjons. Chacun de ces coffres ou barils contiennent entre 1 et 3 émeraudes ! Si vous en trouvez un, ouvrez-le vite, car il disparaîtra pour tout le monde une fois son contenu récupéré !
                Il n’y a pas que les coffres et barils de la cité qui sont affectés par cette bénédiction : les coffres générés naturellement par le monde contiennent à présent des émeraudes en plus de leur butin habituel, alors motivez-vous à les trouver ! </p>
