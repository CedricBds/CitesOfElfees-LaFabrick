                Le labyrinthe fonctionne de la façon suivante : Chaque aventurier doit payer un lot d’entrée en émeraude, dont une partie rejoindra le Jackpot final !. Une fois à l’intérieur, vous disposerez de précisément 743 pas pour trouver la sortie et remporter le gros lot ! En chemin, vous pourrez trouver des vieux coffres comportant des émeraudes poussiéreuses…ou peut-être même une pièce oubliée par tous...
                Une fois le labyrinthe réussi par un aventurier, celui-ci ne pourra plus gagner le gros-lot.
            </p>
            <h1 class="titleActivities">Les succès
            </h1>
            <p>Nous avons décidé de pimenter un peu minecraft durant notre événement ! Tous les succès de base donnent désormais des émeraudes, c’est cadeau ! Mais nous ne nous sommes pas arrêtés là : nous avons incorporé au jeu plus de 44 succès propres à la cité, dont 9 sont secrets ! Débloquez-les en tuant des boss, trouvant des oeufs cachés, découvrant des zones, en achetant une habitation, en complétant un labyrinthe, en découvrant une des reliques… Le nombre d’émeraudes qu’ils donneront dépendra de leur difficulté ! Appuyez sur L en jeu pour afficher le menu des succès ! </p>

