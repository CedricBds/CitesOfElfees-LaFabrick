            <h1 class="titleActivities">/Spawn
            </h1>
            <p>Si vous vous retrouvez bloqués par le décors durant les donjons ou votre exploration de la cité, vous aurez accès à la commande /spawn pour retourner au point d’apparition de votre zone. Cette commande ne fonctionne évidemment pas dans les zones PvP. </p>
            <h1 class="titleActivities">Désactivez les nuages.

            </h1>
            <p>Pour un meilleur rendu, il est vivement recommandé de désactiver les nuages de base du jeu. Plusieurs de nos cartes sont hautes et les nuages peuvent vous aveugler ! </p>
            <h1 class="titleActivities">Jouez avec des shaders est recommandé.

            </h1>
            <p>Nos plus belles constructions sont construites avec des shaders et ils sont recommandés pour une expérience optimale ! </p>
            <h1 class="titleActivities">Augmentez votre distance de rendu au maximum possible.
            </h1>
            <p>La plupart de nos constructions sont si immenses que nous recommandons vivement d’augmenter votre distance de rendu en jeu au maximum ! </p>
            <h1 class="titleActivities">Les minimaps sont interdites.

            </h1>
            <p>Afin que tout le monde joue sur un pied d’égalité et afin d’éviter la triche dans les donjons et autres endroits contenant des labyrinthes ou pièces cachées, les minimaps sont malheureusement interdites durant la cité. Nous sommes désolés de tout inconvénient posé par cette restriction, mais elle se trouve tristement nécessaire.</p>
            <h1 class="titleActivities">L’usage d’autologger est interdit.
            </h1>
            <p>Bien que l’AFK soit une partie intégrale et autorisée d’une cité, nous considérons l’autologger comme une triche puisque dépendant d’un logiciel externe à minecraft. Il est donc interdit. </p>
            <h1 class="titleActivities">La triche est interdite.

            </h1>
            <p>Toute forme de triche nécessitant l’usage d’un logiciel externe à minecraft vanilla est interdite. L’usage de pack de texture conférant un avantage injuste est également interdit. Les pack de textures axés PVP sont cependant autorisés. Il est interdit de modifier manuellement les fichiers du jeu tels que le gamma afin d’obtenir un avantage sur les autres participants. </p>
            <h1 class="titleActivities">Les pillageois ne droppent plus d’émeraudes.

            </h1>
            <p>Afin d’éviter la conception de farms à émeraudes automatiques, le drop d'émeraudes des pillageois a été désactivé. </p>
            <h1 class="titleActivities">Achat de maisons.
            </h1>
            <p>Vous pourrez acheter votre propre maison dans la cité afin de stocker vos émeraudes à l'abri ! Pour ce faire, vous n’aurez qu’à cliquer sur la pancarte à l’intérieur avec le montant d’émeraudes requis sur vous. Il existe trois catégories de maisons, les maisons pauvres, les maisons moyennes et les maisons de luxe ! Chaque catégorie est plus grande et plus luxueuse que la précédente. Le prix des maisons est calculé par rapport à l’espace qu’ils offrent. <b><u>Faites attention, vous ne pourrez acheter qu’une seule maison durant toute votre aventure, tentes comprises !</u></b><br> Alors dépêchez-vous avant qu’elles ne soient toutes prises ! </p>
            <h1 class="titleActivities">Les élytres sont désactivés.

            </h1>
            <p>Afin de favoriser les interactions PVP et de rendre le voyage à nouveau dangereux, les élytres sont désactivés durant toute la cité. </p>
            <h1 class="titleActivities">Les enderchests sont désactivés.

            </h1>
            <p>Afin de favoriser les interactions PVP et de forcer les déplacements avec vos émeraudes, les enderchests ont été désactivés durant toute la cité. </p>
            <h1 class="titleActivities">Les champs infinis.

            </h1>
            <p>Afin que vous ne mouriez pas de faim dès votre première journée, des champs de légumes infinis ont été dispersés autour des points d’eau dans la cité. Ce sont des champs qui se replantent automatiquement, donc servez-vous ! </p>
            <h1 class="titleActivities">La banque.

            </h1>
            <p>La banque d’Élésia vous permettra de déposer vos émeraudes durement acquises, mais seulement quand elle ouvrira ses portes ! En attendant, vous devrez les garder en lieu
                sûr… C’est l’équipe qui aura le plus grand nombre d’émeraudes dans leur compte qui sera couronnée gagnante de la cité ! </p>
            <h1 class="titleActivities">Les marchands.
            </h1>
            <p>Les marchands d’Élésia se trouvent au cœur de la cité. Vous saurez les reconnaître grâce à leur nom en jaune, leur marquage sur la carte de la cité grâce et aux bannières vertes représentant une émeraude sur leurs commerces. Vous retrouverez dans la cité:
            <ul class="listRP">
                <li> Un forgeron</li>
                <li> Une épicière </li>
                <li>Un alchimiste</li>
                <li>Un chasseur</li>
            </ul>
            <p> Ces marchands permettent de vendre vos ressources de base. Pour toute autre ressource plus exotique, tels que des reliques ou objets rares, il vous faudra trouver un marché alternatif.
            </p>
            <h1 class="titleActivities">La redstone. </h1>
            <p>Afin d’éviter que les participants ne se terrent dans leurs maisons avec des fermes, la redstone est désactivée dans les maisons en cité. </p>
            <h1 class="titleActivities">La pêche. </h1>
            <p>Dans la même optique que pour la redstone, la pêche en cité est désactivée afin d’éviter de farmer en toute impunité et sans danger. Pour ces mêmes raisons, la pêche est également désactivée en donjon.</p>
            <h1 class="titleActivities">Les usines zéro tick</h1>
            <p>Par soucis de performances et de pudeur, les usines zéro tick sont strictement interdites. </p>
            <h1 class="titleActivities">La mort et votre inventaire</h1>
            <p>En dehors de la cité, mourir causera la perte de votre équipement de façon normale. Ce n’est pas le cas pour une mort en donjon ; vous garderez votre équipement. Il est impossible de mourir dans la cité. </p>
            <p style="text-align: center;"><br><br> Toute l’équipe vous rappelle de jouer de façon conviviale et vous souhaite un bon événement ! :)</p>

